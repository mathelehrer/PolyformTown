static int ensure_dir(const char *path) {
    if (mkdir(path, 0775) == 0) return 1;
    if (errno == EEXIST) return 1;
    return 0;
}

static int write_file(const char *path, const char *contents) {
    FILE *fp = fopen(path, "w");
    if (!fp) return 0;
    fputs(contents, fp);
    return fclose(fp) == 0;
}

int main(void) {
    if (!ensure_dir("preferences")) {
        fprintf(stderr, "failed to create preferences directory: %s\n", strerror(errno));
        return 1;
    }
    if (!write_file("preferences/focus.supertile", focus_supertile)) {
        fprintf(stderr, "failed to write preferences/focus.supertile\n");
        return 1;
    }
    if (!write_file("preferences/overlap.supertile", overlap_supertile)) {
        fprintf(stderr, "failed to write preferences/overlap.supertile\n");
        return 1;
    }
    printf("preferences/focus.supertile\n");
    printf("preferences/overlap.supertile\n");
    return 0;
}
